<h3>In-Store Notifications</h3>

<p>When I first joined the team in late May of 2017, my first task was to design the onboarding experience for In-Store Notifications, a new feature that was enabled by the placement of Bluetooth beacons in each store. The goal of In-Store Notifications was to provide users with helpful reminders as they walked into the store, such as:</p>

<ul>
	<li>fuel rewards that are set to expire soon</li>
	<li>prescriptions ready to pick up</li>
	<li>items on their shopping list</li>
	<li>special deals</li>
</ul>

<p>With a feature that integrates with an external hardware device like a Bluetooth beacon, a lot can go wrong, making the onboarding experience a particularly important one. On iOS, the following things need to be enabled or turned on to make everything work:</p>

<ul>
	<li>Notification permission</li>
	<li>Always On location permission</li>
	<li>Bluetooth</li>
</ul>

<p>On Android, you also have to keep in mind that many users will turn off location services across their entire device as a battery-saving measure.</p>

<h4>How I worked through this task</h4>

<ol>
	<li>I collaborated extensively with developers to understand the technical constraints and the many ways things could go wrong.</li>
	<li>I reviewed how other apps would onboard users to a similar experience (including apps for the Apple Store, Nordstrom, and Whole Foods, among others).</li>
	<li>I sought feedback from multiple other designers, especially the designers who had worked previously on the project.</li>
</ol>

<h4>What I learned</h4>

<ol>
	<li>Onboarding is especially important for a feature like In-Store Notifications where a number of permissions and settings have to be turned on.</li>
</ol>

<h3>Redesigned Onboarding</h3>

<p><a target="_blank" href="https://docs.google.com/presentation/d/1NbV0eJv1psmxFmD6PtQJY_eR4Z180TJ-sE4iOrqovqw/edit?usp=sharing">View Case Study</a> | <a target="_blank" href="https://sferrari.net/wireframes/HV_FS_Onboarding_Enhancements/index.html">Invision Link</a></p>

<p>The app’s onboarding flow totaled 4 screens and was followed by an authentication/account creation flow that a user could bypass only with great effort. We knew it was too long and that the user should be able to see the value of the app right away, not only after logging in or creating an account. We also wanted to iterate on the coachmarks that had been included with the relaunch of the app.</p>

<h4>How I worked through this task</h4>

<ol>
	<li>I dug into the analytics to see how long it was taking users to go through onboarding plus the authentication/account creation flow. I also looked at login failure rates.</li>
	<li>I did a competitive review of the onboarding for other apps in the retail and grocery sector and how apps continued to educate users once they were in the app.</li>
	<li>I created a holistic approach for how we could improve the onboarding experience to get the user into the app faster and still educate them about the app’s feature </li>
	<li>After the new onboarding experience was released, time to complete onboarding went down by 62% (comparing stats year-over-year between March 2017 and March 2018).</li>
</ol>

<h4>What I learned</h4>

<ol>
	<li>Solid data can help win over a client who might be reluctant to change things in the app.</li>
	<li>General onboarding best practice: the best way for users to see the value of the app is to experience it first-hand. Getting them into the app as soon as possible helps achieve that and avoids drop-offs.</li>
</ol>

<h3>Product Locator</h3>

<p><a target="_blank" href="https://docs.google.com/presentation/d/1NbV0eJv1psmxFmD6PtQJY_eR4Z180TJ-sE4iOrqovqw/edit#slide=id.p">View Case Study</a> | <a target="_blank" href="https://sferrari.net/wireframes/HV_FS_Product_Locator/index.html">Invision Link</a></p>

<p>Prior to working with The Nerdery, the client had an existing app that was built using a cross-platform tool. Our first engagement with the client was to rebuild the app natively for iOS and Android. One feature that was dropped was the Product Locator, which helped users find items in the store. Many users complained about its absence and the client wanted to reintroduce it to the app.</p>

<p>My first step was to interview 3 users who had reached out to the client complaining about the missing Product Locator feature. I wanted to understand how they used the feature and what value it provided to them. One interesting insight was that one of the users would use the feature from home as a way to see if the store carried a product for new recipes that she would try out. I would never have thought that a user would use this feature in this way. And this indicated to me that there was more value to the feature than just locating the product in the store (and also that a deals-focused app will be used in unexpected ways).</p>

<h4>How I worked through this task</h4>

<ol>
	<li>After the interviews, I sought to understand what data was available through the API. An important piece of data in the search results was actually missing: the product name. This greatly impacted how the search results were designed, and I included the “section name” in addition to the aisle number in order to provide them with more context about the results (given that we could not tell the user the actual product name).</li>
	<li>I worked with developers to understand how we could interact with Siri and a Google voice search API to enable voice-based searches. Although speech-to-text dictation is available by default on the keyboard, the client desired to make searching by voice more accessible to users.</li>
	<li>I worked closely with the Creative Director to polish the visual design of the feature.</li>
</ol>

<h4>What I learned</h4>

<ol>
	<li>The interviews with Product Locator users served as a good reminder that you can never fully predict how a user will use a product or feature.</li>
	<li>Both Apple and Google help you embed voice search into your apps, but in quite different ways.</li>
</ol>

<h3>In-Store Research</h3>

<p>A year after the launch of the new app, we wanted to see how customers were using the app. While analytics can tell us <em>what</em> is happening in the app, to understand how and why this is happening you have to go further. The goal of this round of research was to understand what improvements could be made to the app, and to inform the app’s future roadmap.</p>

<p>We arranged with the client that we would visit 3 stores in a variety of settings (rural, and two suburban ones in different states). Working in a team of 3 designers (though we did seek to bring along a developer, we did not manage to do so), we would intercept shoppers as they walked into the store and try to find an app user willing to talk to us.</p>

<p>We reserved a booth in the restaurant in the grocery store and would talk to users there. After a brief conversation, we asked the users to show us on their device what was the most recent thing they did in the app, followed by any other frequent actions that they do in the app. We recorded their interactions on the app using a smartphone mounted on a small tripod (a Gorillapod). </p>

<p>Using our recordings and notes, we started building an Airtable of research “nuggets” (<a href="https://medium.com/@tsharon/the-atomic-unit-of-a-research-insight-7bf13ec8fabe#.34h11em65">an approach Tomer Sharon has written about</a>). As more research is conducted, we can add to this Airtable as needed, and organize and sort along any number of vectors (ask me about it!)(some example templates are avalable from Airtable <a href="https://blog.airtable.com/3-user-research-templates-built-by-ux-experts/">here</a>).</p>

<h4>What I learned</h4>

<ol>
	<li>Intercepting customers walking in to the store and finding one willing to talk (even for compensation) can be hard, but worth the effort because you can get a better feeling for the customer and their shopping experience.</li>
	<li>We had a lot of success in our first suburban store, not so much in the rural store, where smartphone usage was much lower.</li>
	<li>We have since decided to build an SDK into the app that will allow us to conduct remote testing with customers across the Midwest.</li>
</ol>



