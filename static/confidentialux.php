<h2>Hy-Vee and The Nerdery</h2>

<p> The Midwest grocery store chain <a href="https://www.hy-vee.com/">Hy-Vee</a> has two native mobile apps. Hy-Vee's Flagship app (<a href="https://itunes.apple.com/us/app/hy-vee/id1133785983?mt=8">App Store</a>|<a href="https://play.google.com/store/apps/details?id=com.hyvee.android">Google Play Store</a>) has deals, coupons, lists, a tool for monitoring a fuel rewards program, and a section for renewing and transferring prescriptions, among other features. The app has over a quarter million monthly active users. Hy-Vee's Market Grille app (<a href="https://itunes.apple.com/us/app/market-grille/id1146933785?mt=8">App Store</a>|<a href="https://play.google.com/store/apps/details?id=com.hyvee.marketgrille">Google Play Store</a>) and <a href="https://marketgrille.hy-vee.com">website</a> are for browsing the menu and placing pickup orders at the Market Grille “grocerant” attached to most of their grocery stores.</p>

<p>The Nerdery completely rebuilt the Flagship app and created the Market Grille app and website. By the time I was brought on to the project both apps had launched and were in the App Store and Google Play Store. I was the Lead UX Designer for both apps (and the Market Grille website) from May 2017 through March 2018. I worked with a business analyst, visual designers, developers, QA engineers, and a project manager on a scrum team. Hy-Vee provided a product owner, though for most of my time on the project the product owner was not directly engaged on the project. The Nerdery also had a Creative Director who oversaw design work for mulitple Hy-Vee workstreams (The Nerdery also did work on the Hy-Vee website and Aisles Online, Hy-Vee's online grocery ordering website).</p>

