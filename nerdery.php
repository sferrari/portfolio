        <div>
          <h3>Origin Story</h3>
          <p>Anoka-Ramsey Community College had not updated their site since 2008. In addition to having an outdated UI, they knew they were in need of better content governance to prevent a repeat of the 8,000 pages that had accumulated on their site.</p>
        </div>

        <div>
          <h3>What We Accomplished</h3>
          <p>Working hand-in-hand with an external content strategist, we interviewed prospective students, current students, staff, and faculty, to understand what each audience needed to accomplish when visiting the website. A tree test helped solidify the new navigation pattern. And the content strategist helped content creators by coming up with voice and tone guidelines and a content governance plan. See more details (especially around the accessbility work we did) at The Nerdery's <a href="https://www.nerdery.com/case-studies/anoka-ramsey-community-college">case study on the project</a>.</p>
        </div>

      </div> <!-- gridwrapper2 -->
      <hr>
      <div>
          <h3>Lessons Learned</h3>
          <ul>
	          <li><strong>Town Hall:</strong> An academic project involves a lot of consensus building and the need to bring along the wider community on the journey. We did that by seeking out stakeholder opinions and holding a Town Hall where we briefed the wider academic community on what we had learned through our research and what our initial designs and site structure looked like.</li>
	          <li><strong>Mobile navigation:</strong> It can be tricky to come up with a mobile navigation pattern that works well on academic sites given their breadth and depth of content. The pattern we ended up with actually borrows a navigation model that was used on iPods of old. We also supplemented the main navigation with an audience-specific navigation for easier access to commonly-used pages. As Peter Morville has said (I believe it was him): ‚ÄúAlways allow multiple ways in.‚Äù</li>
  	        <li><strong>Community college is one step in a journey:</strong> This was something we learned in our user research. We created a page that would help prospects and students see where a degree could lead them. We also came up with an idea of having student success stories that could be represented in the format of cards to help inspire students to see where their journey could lead.</li>
  	        <li><strong>Never lose touch with the dev team:</strong> This has become less of an issue by virtue of the fact that I am now more likely to be part of a scrum team and be able to sit together with devs. But at the completion of the design phase of this project, I became involved full-time in a different project at The Nerdery and I had trouble keeping connected with the dev team after that point. This resulted in a design decision being made without my knowledge that still haunts me (essentially the card flip interaction from the student success story cards was used in an inappropriate way elsewhere on the site that could cause the user quite a deal of frustration while navigating the site). For a long time I tried to convince the client to improve this, but in the end I was not able to do so.</li>
          </ul>
