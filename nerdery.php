        <div>
          <h3>Origin Story</h3>
          <p><a href="https://www.kalahariresorts.com">Kalahari Resorts</a> wanted to update its website to make its visual appeal more in line with its premium waterpark resort experience. They also wanted to make it a mobile-first site and better help customers plan out their visit.</p>
        </div>

        <div>
          <h3>What We Accomplished</h3>
          <p>We interviewed people who visit waterpark resorts like Kalahari Resort and others in the Midwest and on the East coast. From those interviews we understood that customers appreciated having images and videos that let them see what it was like to visit Kalahari Resort. We also a discovered a <b>"job-to-be-done"</b> that was underserved by existing waterpark resort websites: helping the customer plan out their visit in a way that was easily shareable with family and friends who would be coming with them. Thus was born the <a href="https://www.kalahariresorts.com/pennsylvania/wishlist/">shareable wishlist feature.</a></p>
          <p><a href="https://sferrari.net/wireframes/kalahari_mobile_wireframes/index.html">Mobile Prototype</a> | <a href="https://sferrari.net/wireframes/kalahari_desktop_wireframes/index.html">Desktop Prototype</a></p>
        </div>

      </div> <!-- gridwrapper2 -->
      <hr>
      <div>
          <h3>Lessons Learned</h3>
          <ul>
            <li>If you keep digging deeper in customer interviews, you will find ways of serving the customer that will differentiate the website from that of competitors.</li>
            <li>It is imperative to understand how mobile can affect site navigation, especially if you have a "hamburger" menu that hides away the main navigation. Each page needs to have strong in-context navigation that makes sure the user can find their next step even if they don't open the navigation.</li>
            <li>While initially frustrated that the booking engine was out of scope and decidedly not mobile-friendly, I came to appreciate how much we still had in our control to create an excellent experience for the customer.</li>
            <li>Thorough tree testing (we had around 3,000 responses to our survey!) can help the client make better decisions about site architecture and labeling.</li>

          </ul>
        </div>
      
