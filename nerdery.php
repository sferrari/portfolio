        <div>
          <h3>Origin Story</h3>
          <p>Most ERPs have the following flaws:</p>
          <ol>
            <li>they are on-premise</li>
            <li>they aren't easily customizable</li>
            <li>they are made for highly-centralized retailers</li>
          </ol>
          <p>Our co-development partner was a national retailer that allowed for a great amount of independence in each of its regions and even at the store level. They also had the need to quickly and easily add new attributes (like gluten-free, for example) as they brought in new types of products. We sought to build an ERP that would fit their needs, and most likely the needs of many 21st century companies.</p>
        </div>

        <div>
          <h3>What We Accomplished</h3>
          <p>Working in separate workstreams, we researched, designed, and built out separate parts of the overall Item Management component of the ERP, followed by other components. I led UX first on the Hierarchy team, then on the Supplier Portal team, and finally I supported the UX lead on the Pricing team. It was satisfying to conduct research with users at different retailers and suppliers, produce designs that matched their workflows and mental models, and then test with those same users.</p>
          <p>Today the Item Management piece of the ERP is being used everyday by the co-development partner. The Nerdery remains engaged on the project, but the designers have ramped off after helping to build up the client-side design team.</p>
        </div>

      </div> <!-- gridwrapper2 -->
      <hr>
      <div>
          <h3>Lessons Learned</h3>
          <p>This was my first project at such a large-scale: around 40 developers and 7 designers from The Nerdery were involved, with more on the client side as well. It was also my first project to follow an agile methodology (excuse me, I mean, an agile mindset üòâ), though really we did run through a few flavors of agile. I enjoyed working closely with the product owner and the developers. The sprint rhythm could be challenging from a design perspective, but so long as we stayed a sprint or so ahead and collaborated closely with the developers, it worked out fine.</p> 

          <p>Lastly, from a user research perspective, we really benefitted from having a co-development partner who was eager to make sure the ERP would work well for their employees. Access to these users proved crucial in testing a complex piece of software.</p>
      </div>
            
