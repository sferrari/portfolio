            <li>When an app is geared around coupons and deals (and not shopping), customers will use tools like the Product Locator in ways that might be unexpected.</li>
            <li>Onboarding is especially important for a feature like In-Store Notifications where a number of permissions and settings have to be turned on.</li>
            <li>Intercepting customers in the store can be hard, but it's worth doing occasionaly in order to understand their experience of the store better.</li>
            <li>Educating the user about an "invisible" feature like Siri integration is important, but you need to educate them in a way that doesn't interfere with their usage of the app.</li>
