      <div class="row">
        <div class="col-xs-12 col-sm-12 col-lg-12">
          <h3>Lessons Learned</h3>
          <p>Designing for learners involves different considerations. Where regular users might want to get through their tasks as quickly as possible, learners won't be able to learn efficiently if software is not optimized for their needs. Software designed for learners needs to be structured or "scaffolded" in ways that give time and space for users to make connections and gain fluency with whatever they are are learning. Eventually, as learners make progress and gain expertise, this scaffolding can fall away.</p>
        </div>
      </div><br>
