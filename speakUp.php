          <h3>The Problem</h3>
          <p>People have to give presentations all the time: for work, for school, for any number of occasions. But what is the best way to give a talk? How do you practice effectively? And how can you help people deal with the anxiety they frequently have when they have to present? SpeakUp! is an app that addresses these issues, using a 10-minute TED Talk as the presentation genre it targets.</p>
        </div>
        <div class="col-xs-12 col-sm-6 col-lg-6">
          <h3>Results</h3>
          <p>Over the course of the semester, we brainstormed and sketched solutions, made wireframes and iterated on them, made a somewhat <a href="http://pf9pe2.axshare.com/#p=library&c=1">interactive prototype</a>, and produced some hi-fidelity comps. The end result is an app that helps user learn what makes for an effective talk, helps them build the structure of a talk, and gives them space to practice (in an environment that simluates where they will give their talk) while getting feedback from other users.</p>
        </div>


      </div><br>
