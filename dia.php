        <h3>Process and Details</h3>
                    
        <p>For my <a href="https://www.si.umich.edu/careers/alternative-spring-break">Alternative Spring Break</a> in 2014, I volunteered for a week at the <a href="http://www.dia.org">Detroit Institute of Art's</a> <a href="http://www.dia.org/learn/research.aspx">Research Library</a>. The library is undergoing a transition to a new online library catalog. They, and the rest of the libraries belonging to the <a href="http://www.dalnet.lib.mi.us">Detroit Area Library Network</a>, are moving from <a href="http://www.sirsidynix.com/products/horizon">SirsiDynix Horizon</a> to <a href="http://www.sirsidynix.com/products/portfolio">SirsiDynix Portfolio</a>. As part of that transition, they needed to rethink the presentation of their library catalog homepage, namely the placement of links and other materials, but also what search facets and features to include.</p>

        <p>My job was to evaluate the current state of the library catalog, recommend changes and additions, and then implement those recommendations as much as possible within the week I was at the DIA. In addition to my current studies at UMSI in Human-Computer Interaction, I have a background in academic research, so I drew upon both of these fields in writing up my recommendations.</p> 

        <p>In that one week on-site at the DIA I was able to accomplish a rather thorough needs assessment and usability evaluation, which generated my recommendations. I performed:
          <ul>
            <li>a comparative analysis (looking at other art museum library catalogs like <a href="http://www.frick.org/research/library">the Frick</a>, <a href="http://www.getty.edu/art/browsability">the J. Paul Getty Museum</a>, the <a href="http://www.dma.org">Dallas Museum of Art</a>, and the <a href="http://www.imamuseum.org">Indianapolis Museum of Art</a>, as well as other search engines for artwork),</li> 
            <li>a heuristic analysis,</li> 
            <li>an analysis of the catalog analytics,</li> 
            <li>and before and after user testing through <a href="http://peek.usertesting.com">Peek by UserTesting.com</a> (note: Peek has been discontinued).</li> 
          </ul>
        </p>

        <p>My main recommendations concerned the layout of the catalog homepage, most of which I was able to implement during that week, though SirsiDynix Portfolio only allows a certain amount of freedom in terms of how much you can modify the HTML and CSS of the page, and you have to choose from a set number of templates (i.e. two columns, three columns, etc.). The goal of the catalog homepage is to orient the user to the resources available to them at the library and to facilitate access to those resources. With these goals in mind, I re-architected the catalog homepage to give it a cleaner layout and a better sense of organization. You can see the final results of my work <a href="http://dalnet.ent.sirsi.net/client/dia">here</a> and in the screenshot below.</p><br>
          
