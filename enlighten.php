          <h3>Client</h3>
          <p>The client is a large healthcare system in Ohio, repeatedly named by Thomson Reuters as one of the ten best healthcare systems in the United States.</p><br>
        </div></div>

        <div class="col-sm-6 col-md-3 col-lg-3"><div class="well">
          <h3>Project Goal</h3>
          <p>This project aimed to improve the experience of searching for a doctor by implementing an advanced form of auto-completion and including more search facets in the primary search interface.</p>
        </div></div>

        <div class="clearfix visible-sm"></div>

        <div class="col-sm-6 col-md-3 col-lg-3"><div class="well">
          <h3>Skills Applied and Tools Used</h3>
          <p>Heuristic and comparative analysis, wireframing, information architecture<br></p>
        </div></div>


        <div class="col-sm-6 col-md-3 col-lg-3"><div class="well">
          <h3>Duration and Role</h3>
          <p>May-August 2014<br>
          Lead UX<br></p>
        </div></div>

     </div>
      
      <hr>
      <div class="row">
        <div class="col-xs-12">
          <h3>The Problem</h3>
          <p>The client, and the agency in charge of their digital strategy at the time, came to Enlighten with the overall strategic vision of what they wanted to accomplish by redesigning Find A Doctor experience. My job was to look at the experience from a design standpoint and figure out what needed to be updated and how to do so in order to meet that strategic vision.</p>


        </div>
        <div class="col-xs-12">
          <h3>Results</h3>
          <p>I performed a heuristic analysis of the current search experience and presented my findings to the client. Based on those findings and some comparative analysis done by our strategy partner, I began developing wireframes for the desktop experience. I also pushed for considering what the mobile experience would be like and eventually completed wireframes for the mobile experience.</p>

        </div>


      </div>
      <hr>
      <div class="row">
        <div class="col-xs-12">
          <h3>Lessons Learned</h3>
          <p>Search is hard. And searching within a medical context is even harder. This is where a robust auto-complete function can help the user a lot. A user can start typing either a doctor's name or a condition in the same box, and the auto-complete can display a structured list of possibilities, broken out into different categories (by doctor name, by condition, by treatment, etc.)(see <a href="http://www.healthgrades.com">healthgrades.com</a> for a good example of how this might work). This functionality helps overcome cases where users might not know the exact name of the medical term or specialty they need to search for. </p>

          <p>I also learned that working with a separate agency in charge of strategy can be a bit difficult to manage. Ultimately, design and strategy are so integrated that having separate agencies for each ends up slowing down the project. Fortunately, the client has decided to entrust Enlighten with both design and strategy going forward. And going through this Find A Doctor experience has led them to decide that a redesign of the entire site should be pursued (in lieu of a piecemeal approach).</p>

