      <h2>Helper Helper app for iOS & Android <small>UI Recommendations and Backend IA</small></h2>
      <br>
      <div class="row">
        
        <div class="col-sm-6 col-md-3 col-lg-3"><div class="well">
          <h3>Client</h3>
          <p><a href="https://www.helperhelper.us/app/">Helper Helper</a> helps student-athletes track their volunteering hours and gives university athletic departments a way of showing their impact on the community.</p>
        </div></div>


        <div class="col-sm-6 col-md-3 col-lg-3"><div class="well">
          <h3>Project Goal</h3>
          <p>After the pilot launch, the client noticed users having trouble with finding buttons. She also wanted to add the ability for users to create their own volunteering events on the app.</p>
        </div></div>

        <div class="clearfix visible-sm"></div>

        <div class="col-sm-6 col-md-3 col-lg-3"><div class="well">
          <h3>Skills Applied and Tools Used</h3>
          <p>Wireframing, Information Architecture, Axure<br></p>
        </div></div>  

        <div class="col-sm-6 col-md-3 col-lg-3"><div class="well">
          <h3>Duration and Role</h3>
          <p>Fall 2014<br>
          Lead User Experience for this revision of the app and admin website<br></p>
        </div></div>

     </div>
      
      <hr>
      <div class="row">
        <div class="col-xs-12">
          <h3>The Problem</h3>
          <p>The Helper Helper app was initially designed and developed by Enlighten not long after iOS 7 brought flat design to iOS devices (Windows Phone 7 and Android having already introduced flat design to their platforms before that). Helper Helper's application of flat design principles might have been pushed a bit too far in the initial launch, causing users to mistake buttons for labels.</p>

          <p>Another important change was adding the ability for users to create their own events. Student-athletes frequently seek out opportunities on their own to volunteer, and the app during the pilot round limited them to events that were created by team administrators. While a seemingly simple addition, this feature had ramifications for how events are recorded in the database and represented on the admin website (there were a number of other requests from the client for adding capability to the admin website, but this was one of the main ones).</p>



        </div>
        <div class="col-xs-12">
          <h3>Results</h3>
          <p>The client said that once users were shown how to sign up for volunteering opportunities and then record their time, they could use the app easily. The trouble was when users did not have direct guidance. To me this indicated that their were problems with the UI more so than with the user flow or the app's architecture. And in my first experience of using the app, I <em>too</em> had problems  identifying what was tappable and what wasn't. Together with the team we came up with a way of making the buttons more "button-like" (that is, that it more clearly affords the activity of tapping). For the most part, this involved adding more defined borders to buttons and changing the text of some labels, while still maintaining the appeal of the flat design.</p>

          <p>The challenge of introducing the feature whereby students could add their own event was twofold: first, understanding what the ideal user flow should be on the app itself; and secondly, representing these events on the admin website in a way that wouldn't flood the database with a huge number of student-created events. The solution we came up with (which is difficult to describe in detail here) involved allowing students to create new opportunities associated with already-recognized organizations (present in the database already), and, if the organization was new to the system, containing those volunteer opportunities to a certain section of the admin database.</p>

          <p>The revised version of the app has yet to be released, but when it is I will post some screenshots here.</p>

        </div>


      </div>
      <hr>
      <div class="row">
        <div class="col-xs-12">
          <h3>Lessons Learned</h3>
          <p>Since we were revising a currently existing app and admin website, the changes had to be considered very carefully so that the changes were in keeping with the existing UI and database architecture. This proved challenging, but was a good reminder that subtle but smart changes can make a big difference in improving usability and adding functionality. It's also a good reminder that there is no such thing as a "quick fix" - everything has to be thought through carefully to ensure the best experience for users and administrators alike.</p>
